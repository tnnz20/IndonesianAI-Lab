#perulangan menggunakan function range

#range untuk perulangan sampai dengan 5
for i in range(5):
    print(i)

print("DONE\n")

#range untuk nilai dengan awalan 1-9
for i in range (1,10):
    print(i)

print("DONE\n")

#range untuk diawal dengan angka 2 sampai 20 dengan ditambah 2
for i in range (2 , 20 , 2):
    print(i)

print("DONE")